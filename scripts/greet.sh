#!/bin/bash
# =============================================================================
# greet.sh - 사용자 정보를 출력하는 인사 스크립트
# 작성자: ibetter
# 작성일: 2026-04-24
# =============================================================================

# 변수 선언
readonly GREETING="안녕하세요"
user_name=$(whoami)
current_dir=$(pwd)

# 메인 로직
echo "${GREETING}, ${user_name}님!"
echo "현재 디렉토리: ${current_dir}"
echo "현재 시각: $(date '+%Y-%m-%d %H:%M:%S')"

# 종료
exit 0